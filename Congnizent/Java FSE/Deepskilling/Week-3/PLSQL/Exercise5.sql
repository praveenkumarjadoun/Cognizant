-- AFTER INSERT: AuditLog contains row -> LogID: 1, TransactionID: 10, AccountID: 1, Amount: 150, TransactionType: 'Deposit', LogDate: 2026-07-09
*/


--------------------------------------------------------------------------------
-- Scenario 3: Enforce business rules on deposits and withdrawals.
-- Question: Write a trigger CheckTransactionRules that ensures withdrawals do not exceed the balance and deposits are positive before inserting a record into the Transactions table.
--------------------------------------------------------------------------------

CREATE OR REPLACE TRIGGER CheckTransactionRules
BEFORE INSERT ON Transactions
FOR EACH ROW
DECLARE
    v_balance NUMBER;
BEGIN
    -- Enforce positive amount check for all transactions
    IF :new.Amount <= 0 THEN
        RAISE_APPLICATION_ERROR(-20010, 'Transaction amount must be strictly positive.');
    END IF;

    -- Enforce withdrawal limits
    IF :new.TransactionType = 'Withdrawal' THEN
        -- Get the current balance of the account
        SELECT Balance INTO v_balance FROM Accounts WHERE AccountID = :new.AccountID;

        IF v_balance < :new.Amount THEN
            RAISE_APPLICATION_ERROR(-20011, 'Insufficient balance. Transaction aborted.');
        END IF;
    END IF;
END;
/

