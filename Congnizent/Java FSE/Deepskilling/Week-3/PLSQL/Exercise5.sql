-- Customer ID 1: Name = 'John Doe', LastModified = 2026-07-08

-- AFTER UPDATE (UPDATE Customers SET Name = 'Johnathan Doe' WHERE CustomerID = 1):
-- Customer ID 1: Name = 'Johnathan Doe', LastModified = 2026-07-09 (Auto-updated by trigger)
*/


--------------------------------------------------------------------------------
-- Scenario 2: Maintain an audit log for all transactions.
-- Question: Write a trigger LogTransaction that inserts a record into an AuditLog table whenever a transaction is inserted into the Transactions table.
--------------------------------------------------------------------------------

-- Setup: Create AuditLog table to record all transactions
CREATE TABLE AuditLog (
    LogID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TransactionID NUMBER,
    AccountID NUMBER,
    Amount NUMBER,
    TransactionType VARCHAR2(10),
    LogDate DATE
);

CREATE OR REPLACE TRIGGER LogTransaction
AFTER INSERT ON Transactions
FOR EACH ROW
BEGIN
    INSERT INTO AuditLog (TransactionID, AccountID, Amount, TransactionType, LogDate)
    VALUES (:new.TransactionID, :new.AccountID, :new.Amount, :new.TransactionType, SYSDATE);
END;
/

